// Project Toggle Functionality
document.addEventListener('DOMContentLoaded', function() {
    const toggleButtons = document.querySelectorAll('.toggle-btn');
    const daProjects = document.getElementById('da-projects');
    const sdeProjects = document.getElementById('sde-projects');
    
    toggleButtons.forEach(button => {
        button.addEventListener('click', function() {
            // Remove active class from all buttons
            toggleButtons.forEach(btn => btn.classList.remove('active'));
            
            // Add active class to clicked button
            this.classList.add('active');
            
            // Get the project type
            const projectType = this.getAttribute('data-type');
            
            // Show/hide appropriate project sections
            if (projectType === 'da') {
                daProjects.style.display = 'grid';
                sdeProjects.style.display = 'none';
            } else if (projectType === 'sde') {
                daProjects.style.display = 'none';
                sdeProjects.style.display = 'grid';
            }
        });
    });
});

