// Active Navigation Link Highlighting
const sections = document.querySelectorAll('section[id]');
const navLinks = document.querySelectorAll('.nav-link');

window.addEventListener('scroll', () => {
    let current = '';
    sections.forEach(section => {
        const sectionTop = section.offsetTop;
        const sectionHeight = section.clientHeight;
        if (window.pageYOffset >= sectionTop - 200) {
            current = section.getAttribute('id');
        }
    });

    navLinks.forEach(link => {
        link.classList.remove('active');
        if (link.getAttribute('href') === `#${current}`) {
            link.classList.add('active');
        }
    });
});

// Counter Animation for Hero Stats
function animateCounters() {
    const counters = document.querySelectorAll('.stat-number');
    const speed = 200;

    counters.forEach(counter => {
        const updateCount = () => {
            const target = +counter.getAttribute('data-target') || parseInt(counter.textContent);
            const count = +counter.textContent.replace(/[^\d]/g, '');
            const inc = target / speed;

            if (count < target) {
                counter.textContent = Math.ceil(count + inc);
                setTimeout(updateCount, 1);
            } else {
                counter.textContent = target;
            }
        };
        updateCount();
    });
}

// Initialize counter animation when hero section is visible
const heroObserver = new IntersectionObserver((entries) => {
    entries.forEach(entry => {
        if (entry.isIntersecting) {
            animateCounters();
            heroObserver.unobserve(entry.target);
        }
    });
}, { threshold: 0.5 });

const heroSection = document.querySelector('.hero');
if (heroSection) {
    heroObserver.observe(heroSection);
}

// Add data-target attributes to stat numbers
document.querySelectorAll('.stat-number').forEach(stat => {
    const text = stat.textContent;
    const number = parseInt(text.replace(/[^\d]/g, ''));
    stat.setAttribute('data-target', number);
    stat.textContent = '0';
});

